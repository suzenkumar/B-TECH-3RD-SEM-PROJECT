class DataTypes {
    public static void main(String args[]) {
        char a = 'G';
        int i = 69;         // Removed quotes for integer value
        byte b = 4;         // Removed quotes for byte value
        short s = 56;       // Changed variable name to 's' and removed quotes
        double d = 4.733456;  // Removed quotes and fixed the decimal value
        float f = 4.43327367f; // Removed quotes and added 'f' to indicate a float literal

        System.out.println("Character: " + a);
        System.out.println("Integer: " + i);
        System.out.println("Byte: " + b);
        System.out.println("Short: " + s);
        System.out.println("Double: " + d);
        System.out.println("Float: " + f);
    }
}